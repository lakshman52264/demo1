class calculator:
    def add(self, x, y):
        return x + y

    def subtract(self, x, y):
        return x - y

    def multiply(self, x, y):
        return x * y

    def divide(self, x, y):
        return x / y

    def mod(self, x, y):
        return x % y

    def ppower(self, x, y, z):
        return (x ** y) % z